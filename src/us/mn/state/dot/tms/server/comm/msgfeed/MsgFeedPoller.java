package us.mn.state.dot.tms.server.comm.msgfeed;

import us.mn.state.dot.tms.server.ControllerImpl;
import us.mn.state.dot.tms.server.IDebugLog;
import us.mn.state.dot.tms.server.comm.CommMessage;
import us.mn.state.dot.tms.server.comm.MessagePoller;
import us.mn.state.dot.tms.server.comm.Messenger;

/**
 * Msg feed poller, which periodically retrieves DMS messages
 * generated by an external system via the specified URL.
 *
 * @author Douglas Lau
 * @author Michael Darter
 */
public class MsgFeedPoller extends MessagePoller {

	/** Feed debug log */
	static protected final IDebugLog FEED_LOG = new IDebugLog("feed");

	/** Log a message to the debug log */
	static public void log(String msg) {
		FEED_LOG.log(msg);
	}

	/** Feed ID */
	private final String feed_id;

	/** Create a new poller */
	public MsgFeedPoller(String n, Messenger m) {
		super(n, m);
		feed_id = n;
	}

	/** Create a new message for the specified controller, 
	 *  called by MessagePoller.doPoll(). */
	public CommMessage createMessage(ControllerImpl c) {
		return new Message(messenger);
	}

	/** Check if a drop address is valid */
	public boolean isAddressValid(int drop) {
		return true;
	}

	/** Query message feed */
	public void queryMessages(ControllerImpl c) {
		new OpReadMsgFeed(c, feed_id).start();
	}
}
